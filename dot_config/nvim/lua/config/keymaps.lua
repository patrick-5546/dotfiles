-- resume telescope search
vim.keymap.set(
  "n",
  "<leader>sx",
  require("telescope.builtin").resume,
  { noremap = true, silent = true, desc = "Resume" }
)

-- can use <C-c> to save edits to multiple rows
vim.keymap.set("i", "<C-c>", "<Esc>")
